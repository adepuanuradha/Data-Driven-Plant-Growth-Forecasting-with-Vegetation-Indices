from sklearn.preprocessing import LabelEncoder

from matplotlib import pyplot as plt
import seaborn as sns


# Function to define health categories based on NDVI values
def categorize_health(ndvi):
    if -1 <= ndvi < 0:
        return "Dead"
    elif 0 <= ndvi <= 0.33:
        return "Unhealthy"
    elif 0.33 < ndvi <= 0.66:
        return "Moderately Healthy"
    elif 0.66 < ndvi <= 1:
        return "Very Healthy"
    else:
        return "Unknown"


# Function to extract RGB color features from an image
def extract_image_features(image_path):

    if not os.path.exists(image_path):
        raise ValueError(
            f"File not found: {image_path}. Please check the file path."
        )

    image = cv2.imread(image_path)

    if image is None:
        raise ValueError(
            f"Cannot read the image from {image_path}. Please ensure it's a valid image."
        )

    image = cv2.resize(image, (128, 128))

    avg_color = np.mean(image, axis=(0, 1))

    return avg_color[::-1]  # Reverse BGR to RGB


# Function to update or create a dataset with new entries
def update_dataset(
    image_path,
    ndvi,
    plant_name,
    dataset_path="plant_health_dataset.csv"
):

    try:
        rgb_features = extract_image_features(image_path)

    except ValueError as e:
        print(e)
        return None

    health_category = categorize_health(ndvi)

    data = {
        "Plant Name": plant_name,
        "R": rgb_features[0],
        "G": rgb_features[1],
        "B": rgb_features[2],
        "NDVI": ndvi,
        "Health": health_category,
    }

    if os.path.exists(dataset_path):

        df = pd.read_csv(dataset_path)

        new_row_df = pd.DataFrame([data])

        df = pd.concat(
            [df, new_row_df],
            ignore_index=True
        )

    else:
        df = pd.DataFrame([data])

    df.to_csv(dataset_path, index=False)

    print("Dataset updated successfully!")

    return df


# Function to load an existing dataset
def load_dataset(
    dataset_path="plant_health_dataset.csv"
):

    if not os.path.exists(dataset_path):
        print("Dataset not found! Please create it first.")
        return None

    df = pd.read_csv(dataset_path)

    print(f"Loaded dataset with {len(df)} entries.")

    return df


# Encode the 'Health' column for machine learning purposes
def encode_health_column(df):

    encoder = LabelEncoder()

    df["Health"] = encoder.fit_transform(
        df["Health"]
    )

    return df


